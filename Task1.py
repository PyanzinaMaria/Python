#Задача Иосифа Флавия – широко известная в информатике теоретическая задача.
#Суть ее заключается в следующем: в круг выстраивается n человек.
#Из стоящих выбирается k-й человек, который покидает круг. Далее покинувшие круг
#не участвуют в процессе подсчета, а последний оставшийся считается победителем.
# Цель состоит в том, чтобы разработать функцию, которая принимает заданные
# значения n и k и возвращает порядок, в котором игроки будут покидать круг.
# Здесь n представляет общее количество людей, а k – количество пропусков.
# Важно отметить, что k может быть меньше, равно или даже больше n.

def iosif(n: int, k: int) -> list:
    # Создаем список участников от 1 до n
    people = list(range(1, n + 1))
    result = []
    index = 0

    # Пока список не пуст
    while people:
        # Находим индекс следующего выбывающего участника
        index = (index + k - 1) % len(people)
        # Удаляем участника и добавляем его в результат
        result.append(people.pop(index))

    return result

print("n=6, k=2", iosif(6, 2))
print("n=5, k=2", iosif(5, 2))
print("n=8, k=8", iosif(8, 8))
print("n=3, k=9", iosif(3, 9))
print("n=4, k=3", iosif(4, 3))
